module tb_Mux4Way16;

  logic [15:0] a;
  logic [15:0] b;
  logic [15:0] c;
  logic [15:0] d;
  logic [ 1:0] sel;
  logic [15:0] out;

  Mux4Way16 dut (
      .a  (a),
      .b  (b),
      .c  (c),
      .d  (d),
      .sel(sel),
      .out(out)
  );

  task automatic check(input logic [15:0] i1, input logic [15:0] i2, input logic [15:0] i3,
                       input logic [15:0] i4, input logic [1:0] s, input logic [15:0] exp);
    a   = i1;
    b   = i2;
    c   = i3;
    d   = i4;
    sel = s;
    #1;
    if (out !== exp) begin
      $display("FAIL! Input: i1=%b, i2=%b, i3=%b, i4=%b s=%b | Expected: %b | Got: %b", i1, i2, i3,
               i4, s, exp, out);
    end else begin
      $display("PASS: i1=%b, i2=%b, i3=%b, i4=%b s=%b -> out=%b", i1, i2, i3, i4, s, out);
    end
  endtask


  initial begin
    check(16'b0000000000000000, 16'b0000000000000000, 16'b0000000000000000, 16'b0000000000000000,
          2'b00, 16'b0000000000000000);
    check(16'b0000000000000000, 16'b0000000000000000, 16'b0000000000000000, 16'b0000000000000000,
          2'b01, 16'b0000000000000000);
    check(16'b0000000000000000, 16'b0000000000000000, 16'b0000000000000000, 16'b0000000000000000,
          2'b10, 16'b0000000000000000);
    check(16'b0000000000000000, 16'b0000000000000000, 16'b0000000000000000, 16'b0000000000000000,
          2'b11, 16'b0000000000000000);
    check(16'b0001001000110100, 16'b1001100001110110, 16'b1010101010101010, 16'b0101010101010101,
          2'b00, 16'b0001001000110100);
    check(16'b0001001000110100, 16'b1001100001110110, 16'b1010101010101010, 16'b0101010101010101,
          2'b01, 16'b1001100001110110);
    check(16'b0001001000110100, 16'b1001100001110110, 16'b1010101010101010, 16'b0101010101010101,
          2'b10, 16'b1010101010101010);
    check(16'b0001001000110100, 16'b1001100001110110, 16'b1010101010101010, 16'b0101010101010101,
          2'b11, 16'b0101010101010101);
  end

endmodule
