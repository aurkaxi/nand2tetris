module tb_Mux8Way16;

  logic [15:0] a, b, c, d, e, f, g, h;
  logic [ 2:0] sel;
  logic [15:0] out;

  Mux8Way16 dut (
      .a  (a),
      .b  (b),
      .c  (c),
      .d  (d),
      .e  (e),
      .f  (f),
      .g  (g),
      .h  (h),
      .sel(sel),
      .out(out)
  );

  task automatic check(input logic [15:0] i1, input logic [15:0] i2, input logic [15:0] i3,
                       input logic [15:0] i4, input logic [15:0] i5, input logic [15:0] i6,
                       input logic [15:0] i7, input logic [15:0] i8, input logic [2:0] s,
                       input logic [15:0] exp);
    a   = i1;
    b   = i2;
    c   = i3;
    d   = i4;
    e   = i5;
    f   = i6;
    g   = i7;
    h   = i8;
    sel = s;
    #1;
    if (out !== exp) begin
      $display(
          "FAIL! Input: i1=%b, i2=%b, i3=%b, i4=%b, i5=%b, i6=%b, i7=%b, i8=%b, s=%b | Expected: %b | Got: %b",
          i1, i2, i3, i4, i5, i6, i7, i8, s, exp, out);
    end else begin
      $display("PASS: i1=%b, i2=%b, i3=%b, i4=%b, i5=%b, i6=%b, i7=%b, i8=%b, s=%b -> out=%b", i1,
               i2, i3, i4, i5, i6, i7, i8, s, out);
    end
  endtask


  initial begin
    // Case 1: All zeros with different select values
    check(16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 3'b000, 16'b0);
    check(16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 3'b001, 16'b0);
    check(16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 3'b010, 16'b0);
    check(16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 3'b011, 16'b0);
    check(16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 3'b100, 16'b0);
    check(16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 3'b101, 16'b0);
    check(16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 3'b110, 16'b0);
    check(16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 16'b0, 3'b111, 16'b0);

    // Case 2: Multi-pattern data testing each select path
    check(16'b0001001000110100, 16'b0010001101000101, 16'b0011010001010110, 16'b0100010101100111,
          16'b0101011001111000, 16'b0110011110001001, 16'b0111100010011010, 16'b1000100110101011,
          3'b000, 16'b0001001000110100);
    check(16'b0001001000110100, 16'b0010001101000101, 16'b0011010001010110, 16'b0100010101100111,
          16'b0101011001111000, 16'b0110011110001001, 16'b0111100010011010, 16'b1000100110101011,
          3'b001, 16'b0010001101000101);
    check(16'b0001001000110100, 16'b0010001101000101, 16'b0011010001010110, 16'b0100010101100111,
          16'b0101011001111000, 16'b0110011110001001, 16'b0111100010011010, 16'b1000100110101011,
          3'b010, 16'b0011010001010110);
    check(16'b0001001000110100, 16'b0010001101000101, 16'b0011010001010110, 16'b0100010101100111,
          16'b0101011001111000, 16'b0110011110001001, 16'b0111100010011010, 16'b1000100110101011,
          3'b011, 16'b0100010101100111);
    check(16'b0001001000110100, 16'b0010001101000101, 16'b0011010001010110, 16'b0100010101100111,
          16'b0101011001111000, 16'b0110011110001001, 16'b0111100010011010, 16'b1000100110101011,
          3'b100, 16'b0101011001111000);
    check(16'b0001001000110100, 16'b0010001101000101, 16'b0011010001010110, 16'b0100010101100111,
          16'b0101011001111000, 16'b0110011110001001, 16'b0111100010011010, 16'b1000100110101011,
          3'b101, 16'b0110011110001001);
    check(16'b0001001000110100, 16'b0010001101000101, 16'b0011010001010110, 16'b0100010101100111,
          16'b0101011001111000, 16'b0110011110001001, 16'b0111100010011010, 16'b1000100110101011,
          3'b110, 16'b0111100010011010);
    check(16'b0001001000110100, 16'b0010001101000101, 16'b0011010001010110, 16'b0100010101100111,
          16'b0101011001111000, 16'b0110011110001001, 16'b0111100010011010, 16'b1000100110101011,
          3'b111, 16'b1000100110101011);
  end

endmodule
